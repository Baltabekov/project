<h1 style="font-stretch:condensed;font-family:Times New Roman;font-size:32px;margin-top:10px;margin-left:10px;">Жаңалықтар</h1>
<hr>
<h1 style="font-family:Lucida Sans Unicode;margin-left:5px;margin-top:0px;">Қазақстан құрамасы Нидерланды футболшыларына есе жіберді.</h1>
<img src="1443787458eliv7.jpg" style="width:70%;height:400px;margin-left:142px;">
<h3 style="font-weight:bold;margin-left:37px;">Футболдан Қазақстан құрамасы 2016 жылы Францияда өтетін Еуропа чемпионатына іріктеу кезеңі аясында Нидерланды құрамасымен кездесіп, 3:1 есебімен есе жіберді.</h3>   
<h4 style="font-weight:200;font-size:17px;margin-left:37px;">«Амстердам Арена» стадионында өткен ойын аса тартысты болды. Алаң иелері алғашқы минуттардан ойынды өз қолдарына алып, жерлестерімізді тықсыра бастады. Нидерландылық футболшылардың жойқын шабуылдарына бірде қорғаушыларымыз қарсылық көрсетсе, бірде қарағандылық «Шахтердің» қақпашысы Александр Мокин тойтарыс беріп жатты. Алғаш болып қазақстандық футболшылардың сирек жасаған қарымта шабуылдарының бірінде есеп ашылды. Ойынның 17-ші минутында бұрыштамадан көтерілген допты баспен қақпаға тура бағыттаған Ренат Абдуллин Қазақстан құрамасын алға шығарды. Бұл доп қазақстандықтардың биылғы Еуропа чемпионатына іріктеу кезеңіндегі алғашқы голы болды.<br><br>
Екінші кезеңді Нидерланды құрамасының футболшылары белсенді бастады. Ал, Юрий Красножанның командасы өз кезегінде қарсы шабуылдарға иек артып отырды. Толассыз шабуылдармен Александр Мокин қақпасын нысанаға алған Гус Хиддинк шәкірттері дегендеріне жетіп тынды. Клаас-Ян Хунтелаардың шығуымен есеп теңесіп, нидерландылықтардың шабуылына серпін қосылды.</h4>
<img src="2163232_w2_thb_610x350x2.jpg" style="width:70%;height:400px;margin-left:142px;">

<h4 style="font-weight:200;font-size:17px;margin-left:37px;">Нидерланды футболшыларының голынан кейін, арада екі минут өткен кезде Бауыржан Жолшиев қызыл қағаз алып, алаңнан қуылды. Бұл жағдайдан кейін ойын алаң илерінің толықтай иелігіне көшті. Ойынның негізгі бөлігі отандастарымыздың жарты алаңында өрбіді.<br><br>
Нидерланды құрамасы сапынан Ибрахим Афеллай мен Робин ван Перси көзге түсіп, өз командасына жеңіс сыйлады. «Манчестер Юнайтед» командасының шабуылшысы Робин ван Перси он бір метрлік айып добын сәтті орындады.</h4>
<img src="1412976910071_wps_45_VI_Images_via_Press_Assoc.jpg" style="width:70%;height:400px;margin-left:142px;">
